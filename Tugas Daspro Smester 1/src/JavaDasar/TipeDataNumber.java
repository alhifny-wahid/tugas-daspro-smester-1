package JavaDasar;

public class TipeDataNumber {

    public static void main(String[] args) {
        //Integer Number
        byte iniByte = 100;
        short iniShort = 10000;
        int iniInt = 1000000;
        long iniLong = 1000000000;
        long iniLong2 = 1000000000000000000L;

        //Floating Point Number
        float iniFloat = 10.12F;
        double iniDouble = 12.242;

        //Kode Literals
        int iniDecimal = 25; //untuk decimal langsung input angka desimal biasa
        int iniHexadecimal = 0xa132b; //untuk hexa input 0x dulu lalu angka desimalnya
        int iniBinner = 0b01010101; //untuk binner input 0b dulu lalu angka desimalnya

        //Underscore: tidak wajib! hanya untuk pemisah agar tidak bingung ketika menghitung
        int balance = 1_000_0;
        long sum = 60_000_000;

    }
}
