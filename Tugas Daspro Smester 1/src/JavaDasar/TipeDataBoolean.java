package JavaDasar;

public class TipeDataBoolean {
    public static void main(String[] args) {
        //Tipe Data Boolean
        //- Tipe data boolean adalah tipe data yang memiliki 2 nilai, yaitu benar dan salah (true & false)
        //- Tipe data boolean di java di representasikan dengan kata kunci booelan
        //- Nilai benar di representasikan dengan kata kunci true
        //- Nilai benar di representasikan dengan kata kunci false
        //- Default value untuk boolean adalah valse (default: jika tidak memasukkan variabel maka nilainya valse)

        //Kode: Boolean
  
        boolean benar = true;
        boolean salah = false;

        System.out.println(benar);
        System.out.println(salah);
    }
}
