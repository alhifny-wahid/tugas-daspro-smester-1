package JavaDasar;

public class TipeDataChar {

    public static void main(String[] args) {
        //Tipe Data Character
        //- Data Character (huruf) di java di representasikan oleh tipe char
        //- untuk membuat data char di java, kita bisa menggunakan tanda ' (petik satu) di awal dan di akhir karater
        char w = 'W';
        char a = 'A';
        char h = 'H';
        char i = 'I';
        char d = 'D';

        System.out.print(w);
        System.out.print(a);
        System.out.print(h);
        System.out.print(i);
        System.out.print(d);
    }
}
